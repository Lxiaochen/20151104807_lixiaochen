package com.qst.pojos;

public class Outboud {

	private int outboudId; 
	private String outboudName ;
	private String typeName;
	private int number;
	private String remark;
	
	public int getOutboudId() {
		return outboudId;
	}
	public void setOutboudId(int outboudId) {
		this.outboudId = outboudId;
	}
	public String getOutboudName() {
		return outboudName;
	}
	public void setOutboudName(String outboudName) {
		this.outboudName = outboudName;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
